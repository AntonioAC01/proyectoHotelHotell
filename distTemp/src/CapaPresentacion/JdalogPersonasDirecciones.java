    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtDireccion.getText().compareTo("") != 0 && txtNumero.getText().compareTo("") != 0 && txtCorreo.getText().compareTo(" ") != 0) {
            vPersonaDirecciones dts = new vPersonaDirecciones();
            fDireccionesDAO func = new fDireccionesDAO();
            dts.setPersonaDireccionDireccion(txtDireccion.getText());
            dts.setPersonaDireccionTelefono(Integer.parseInt(txtNumero.getText()));
            dts.setPersonaDireccionCorreo(txtCorreo.getText());
            dts.setIdPersona(Integer.parseInt(txtidPersona.getText()));

            if (accion.equals("guardar")) {
                if (func.insertar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "Las Direciones fueron agregadas correctamente");
                    mostrar("");
                    limpiarTextos();
                    inhabilitarTextos();
                    controles(true);
                }

            } else if (accion.equals("editar")) {
                                    dts.setIdPersonaDireccion(Integer.parseInt(txtIdDireciones.getText()));

                if (func.editar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "Las Direciones fueron editas correctamente");
                    mostrar("");
                    limpiarTextos();
                    inhabilitarTextos();
                    controles(true);
                }
            }

        }


    }//GEN-LAST:event_btnGuardarActionPerformed

