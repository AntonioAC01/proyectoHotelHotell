    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtIdDireciones.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Deseas Eliminar la Direcion?", "Confirmar", 2);
            if (confirmacion == 0) {
                fDireccionesDAO func = new fDireccionesDAO();
                vPersonaDirecciones dts = new vPersonaDirecciones();
                dts.setIdPersonaDireccion(Integer.parseInt(txtIdDireciones.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitarTextos();
                controles(false);
                limpiarTextos();
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

